const {exec} = require('child_process');


console.log('pip install -U pip...');
exec('pip install -U pip', (error, stdout, stderr) => {
  if (error) {
    console.error(`exec error: ${error}`);
    return;
  }
  console.log(`stdout: ${stdout}`);
  console.error(`stderr: ${stderr}`);
});

